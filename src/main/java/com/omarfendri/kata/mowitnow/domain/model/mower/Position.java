package com.omarfendri.kata.mowitnow.domain.model.mower;

import lombok.Builder;

@Builder
public class Position {
    int x;
    int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
